triangle_base = float(input('Enter the base of the triangle in sm:\n'))
triangle_height = float(input('Enter the height of the triangle in sm:\n'))

triangle_area = (triangle_base * triangle_height) / 2

print(f'Triangle base is: {triangle_base:.1f}\n'
      f'Triangle height is: {triangle_height:.1f}\n'
      f'Triangle area is {triangle_area:.2f} square sm')
